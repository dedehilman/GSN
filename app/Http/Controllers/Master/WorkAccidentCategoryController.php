<?php

namespace App\Http\Controllers\Master;

use App\Http\Controllers\AppCrudController;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Validator;

class WorkAccidentCategoryController extends AppCrudController
{

    public function __construct()
    {
        $this->setDefaultMiddleware('work-accident-category');
        $this->setDefaultView('master.work-accident-category');
        $this->setSelect('master.work-accident-category.select');
        $this->setModel('App\Models\WorkAccidentCategory');
    }

    public function validateOnStore(Request $request)
    {
        $validator = Validator::make($request->all(), [
            'code' => 'required|max:255|unique:work_accident_categories',
            'name' => 'required|max:255',
        ]);

        if($validator->fails()){
            return $validator->errors()->all();
        }
    }

    public function validateOnUpdate(Request $request, int $id)
    {
        $validator = Validator::make($request->all(), [
            'code' => 'required|max:255|unique:work_accident_categories,code,'.$id,
            'name' => 'required|max:255',
        ]);

        if($validator->fails()){
            return $validator->errors()->all();
        }
    }
}

